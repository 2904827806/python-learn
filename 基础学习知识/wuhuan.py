#绘制奥运五环

import turtle
turtle.width(20)
turtle.color("blue")
turtle.circle(75)


turtle.penup()
turtle.goto(120,0)
turtle.pendown()
turtle.color("black")
turtle.circle(75)

turtle.penup()
turtle.goto(240,0)
turtle.pendown()
turtle.color("red")
turtle.circle(75)

turtle.penup()
turtle.goto(60,-90)
turtle.pendown()
turtle.color("yellow")
turtle.circle(75)

turtle.penup()
turtle.goto(180,-90)
turtle.pendown()
turtle.color("green")
turtle.circle(75)

import turtle
turtle.write("中国万岁")
